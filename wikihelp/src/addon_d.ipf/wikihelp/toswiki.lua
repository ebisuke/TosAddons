*ペットが欲しい！ [#hc30d1bc]
TOSでは騎乗可能なペットをコンパニオンとよびます。
コンパニオン１体につきチームのキャラクター枠を1枠使います。
無料のキャラクター枠４枠使用中の場合は、取得時にTPを支払って枠を追加する必要があります。
//序盤にしてはやや値が張りますが、メリットを感じるのであればコンパニオン商人から購入してもよいでしょう。

-プレイヤーのあとをついてきてくれる
-敵を攻撃してくれる(そこまで強くはない)

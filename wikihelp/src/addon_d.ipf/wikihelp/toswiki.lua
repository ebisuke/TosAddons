レベル制限などもあるため今は弱いと思っても特性でがらりと変わることもあります。

-装備を更新してみる
まずはレベルにあった装備に変えてみる。
序盤は、特定のメインクエスト（黄色い旗のマーク）を進めていくとケドラ商団から装備品が支給されるので積極的にクエストを進めてみましょう。
支給される装備は、各クラスのレアのメイン武器とサブ武器から最大２個、上半身/下半身防具を４個選択可能になっています。一部の武器は、旧仕様のままのクエスト報酬やドロップ品よりも強力で有用な追加効果が付与されている物があります。（同じものを重複して選択したり、自分で使えない武器を選択して別キャラにチーム倉庫で渡すことが出来ます）

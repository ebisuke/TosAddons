PTウィンドウを開いてチャット招待タグ入力ボタンを押すと、チャットにほかの人が参加できるPTリンクを添付できます。
これをクリックすると誰でも即座にパーティに参加できます。（ただし5人まで）

-露店のウィンドウにはhtmlタグを使った装飾なども行えます。
露店は看板をクリックするかキャラクターえおクリックすることで利用できます。


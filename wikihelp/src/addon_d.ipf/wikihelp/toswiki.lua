*体力を回復したい！ [#oe945459]
クレリックは自分にヒールで回復できますが、他の系統ではHPポーションで回復します。
各町にいるマーケット商人を探し、マーケットの消耗品から濃縮ポーションLv15（HP5614回復）を買うとよいでしょう。
